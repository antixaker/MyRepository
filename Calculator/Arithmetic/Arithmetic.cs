        }

        public double Division(double arg1, double arg2)
        {
            return arg1 / arg2;
        }

        #endregion

        #region Decimal

        public decimal Addition(decimal arg1, decimal arg2)
        {
            return arg1 + arg2;
        }

        public decimal Subtraction(decimal arg1, decimal arg2)
        {
            return arg1 - arg2;
        }

        public decimal Multiplication(decimal arg1, decimal arg2)
        {
            return arg1 * arg2;
        }

        public decimal Division(decimal arg1, decimal arg2)
